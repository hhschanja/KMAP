package com.kmap.product;

public class ProductDTO {
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getMaker_num() {
		return maker_num;
	}
	public void setMaker_num(int maker_num) {
		this.maker_num = maker_num;
	}
	private int num;
	private String name;
	private int price;
	private String contents;
	private int count;
	private int maker_num;
	

}
